# Description: Write an infinite loop that prints “Infinite”.
# An infinite loop never ends. The condition is always true.


i = 1  # sets variable equal to 1

while i < 2:  # will check if the variable "i" is less than two
    print('Infinite')  # if the value is less than 2 (True), then the print statement is printed

# Because the while loop is never False and has no parameters, it will run infinitely.
